# Data loading

def load_circumplex(csv_path: Path) -> dict[str, tuple[float, float]]:
    def normalise(s: str) -> str:
        return s.strip().lower().replace("_", " ")
    circumplex: dict[str, tuple[float, float]] = {}
    with open(csv_path, newline="") as f:
        reader = csv.DictReader(f)
        for row in reader:
            circumplex[normalise(row["emotion"])] = (float(row["valence"]), float(row["arousal"]))
    print(f"Loaded {len(circumplex)} VAD entries")
    return circumplex


def lookup_vad(emotion: str, circumplex: dict) -> tuple[float, float] | None:
    return circumplex.get(emotion.strip().lower().replace("_", " "))


def _layer_file(emo_dir: Path, layer: int) -> Path | None:
    for name in (f"layer_{layer}_resid_projected.npy", f"layer_{layer}_resid.npy"):
        p = emo_dir / name
        if p.exists():
            return p
    return None


def detect_layers(vectors_dir: Path, circumplex: dict) -> list[int]:
    """Scan the first emotion sub-directory and return all layer indices found."""
    emotion_dirs = sorted([
        d for d in vectors_dir.iterdir()
        if d.is_dir() and lookup_vad(d.name, circumplex) is not None
    ])
    if not emotion_dirs:
        return []
    found = set()
    for p in emotion_dirs[0].iterdir():
        m = re.match(r"layer_(\d+)_resid(?:_projected)?\.npy", p.name)
        if m:
            found.add(int(m.group(1)))
    return sorted(found)


def load_vectors(
    vectors_dir: Path,
    requested_layers: list[int],
    circumplex: dict,
) -> tuple[dict[int, dict[str, np.ndarray]], list[int]]:
    emotion_dirs = sorted([
        d for d in vectors_dir.iterdir()
        if d.is_dir() and lookup_vad(d.name, circumplex) is not None
    ])

    available_layers = []
    if emotion_dirs:
        for layer in requested_layers:
            if _layer_file(emotion_dirs[0], layer) is not None:
                available_layers.append(layer)
            else:
                print(f"  WARNING: layer {layer} missing in {vectors_dir.name}, skipping")

    vecs: dict[int, dict[str, np.ndarray]] = {l: {} for l in available_layers}
    for emo_dir in emotion_dirs:
        for layer in available_layers:
            path = _layer_file(emo_dir, layer)
            if path is not None:
                vecs[layer][emo_dir.name] = np.load(path).astype(np.float32)

    if available_layers:
        common = set(vecs[available_layers[0]].keys())
        for l in available_layers[1:]:
            common &= set(vecs[l].keys())
        for l in available_layers:
            vecs[l] = {e: vecs[l][e] for e in sorted(common)}
        dim = next(iter(vecs[available_layers[0]].values())).shape[0]
        print(f"  {len(common)} emotions × {len(available_layers)} layers (dim={dim})")

    return vecs, available_layers


def diagnostic_layer_similarities(vecs: dict[int, dict[str, np.ndarray]], layers_to_check: list[int] = None):
    """Check cosine similarities between pooled emotion vectors at different layers."""
    available_layers = sorted(vecs.keys())
    if layers_to_check is None:
        layers_to_check = available_layers
    else:
        layers_to_check = [l for l in layers_to_check if l in available_layers]

    if not layers_to_check:
        print("No layers to check available")
        return

    print("\n[DIAGNOSTIC] Cosine similarities between pooled emotion vectors:")

    common_emotions = set(vecs[layers_to_check[0]].keys())
    for l in layers_to_check[1:]:
        common_emotions &= set(vecs[l].keys())
    common_emotions = sorted(list(common_emotions))

    if not common_emotions:
        print("No common emotions found")
        return

    layer_matrices = {}
    for l in layers_to_check:
        layer_matrices[l] = np.stack([vecs[l][e] for e in common_emotions], axis=0)

    base_layer = layers_to_check[0]
    base_mat = layer_matrices[base_layer]

    for l in layers_to_check:
        if l == base_layer:
            continue
        curr_mat = layer_matrices[l]
        norms_base = np.linalg.norm(base_mat, axis=1, keepdims=True)
        norms_curr = np.linalg.norm(curr_mat, axis=1, keepdims=True)
        norms_base = np.where(norms_base == 0, 1, norms_base)
        norms_curr = np.where(norms_curr == 0, 1, norms_curr)
        cos_sims = np.sum(base_mat * curr_mat, axis=1) / (norms_base.flatten() * norms_curr.flatten())
        mean_cos = np.mean(cos_sims)
        print(f"Layer {l} vs layer {base_layer}: mean cos_sim = {mean_cos:.6f} "
              f"(range: {cos_sims.min():.6f} - {cos_sims.max():.6f})")


def build_contrast(vecs: dict[int, dict[str, np.ndarray]]) -> dict[int, np.ndarray]:
    result = {}
    for l, ev in vecs.items():
        M = np.stack(list(ev.values()), axis=0)
        result[l] = M - M.mean(axis=0, keepdims=True)
    return result


# Core analysis

def run_pca_layer(
    contrast_mat: np.ndarray,
    emotions: list[str],
    circumplex: dict,
) -> dict:
    valences = np.array([lookup_vad(e, circumplex)[0] for e in emotions])
    arousals = np.array([lookup_vad(e, circumplex)[1] for e in emotions])

    pca    = PCA(n_components=min(10, len(emotions) - 1))
    coords = pca.fit_transform(contrast_mat)
    pc1, pc2 = coords[:, 0], coords[:, 1]

    if pearsonr(pc1, valences)[0] < 0:
        pc1 = -pc1
    if pearsonr(pc2, arousals)[0] < 0:
        pc2 = -pc2

    r_val, p_val = pearsonr(pc1, valences)
    r_aro, p_aro = pearsonr(pc2, arousals)

    return {
        "pc1": pc1, "pc2": pc2,
        "valences": valences, "arousals": arousals,
        "r_val": float(r_val), "p_val": float(p_val),
        "r_aro": float(r_aro), "p_aro": float(p_aro),
        "rho_val": float(spearmanr(pc1, valences).correlation),
        "rho_aro": float(spearmanr(pc2, arousals).correlation),
        "ev": pca.explained_variance_ratio_,
    }


def linear_cka(X: np.ndarray, Y: np.ndarray) -> float:
    n = X.shape[0]
    H  = np.eye(n) - np.ones((n, n)) / n
    Xc, Yc = H @ X, H @ Y
    dot  = np.linalg.norm(Yc.T @ Xc, "fro") ** 2
    norm = np.linalg.norm(Xc.T @ Xc, "fro") * np.linalg.norm(Yc.T @ Yc, "fro")
    return float(dot / (norm + 1e-10))


def _label_slug(label: str) -> str:
    """Convert a label like 'Apertus stories' → 'apertus_stories' for use in filenames."""
    return re.sub(r"[^a-z0-9]+", "_", label.lower()).strip("_")


def _valence_direction(
    contrast_mat: np.ndarray,
    emotions: list[str],
    circumplex: dict,
) -> np.ndarray:
    valences = np.array([lookup_vad(e, circumplex)[0] for e in emotions])
    w, _, _, _ = np.linalg.lstsq(contrast_mat, valences, rcond=None)
    return w / (np.linalg.norm(w) + 1e-10)


# Shared panel-drawing helpers

def _split_layers(layers: list[int], n: int = 3) -> list[list[int]]:
    """Divide layers into n roughly equal groups."""
    k = len(layers)
    base, rem = k // n, k % n
    groups, start = [], 0
    for i in range(n):
        size = base + (1 if i < rem else 0)
        if size > 0:
            groups.append(layers[start:start + size])
        start += size
    return groups


def _draw_cka_panel(ax: plt.Axes, mat: np.ndarray, group: list[int], title: str, add_colorbar: bool = True):
    n = len(group)
    im = ax.imshow(mat, cmap="viridis", vmin=0, vmax=1, aspect="auto")
    if add_colorbar:
        plt.colorbar(im, ax=ax, fraction=0.046, pad=0.04, label="linear CKA")
    tick_fs = max(5, 8 - n // 6)
    labels = [str(l) for l in group]
    ax.set_xticks(range(n)); ax.set_xticklabels(labels, fontsize=tick_fs)
    ax.set_yticks(range(n)); ax.set_yticklabels(labels, fontsize=tick_fs)
    ax.set_xlabel("layer"); ax.set_ylabel("layer")
    ax.set_title(title)
    cell_fs = max(4, 7 - n // 6)
    for i in range(n):
        for j in range(n):
            v = mat[i, j]
            ax.text(j, i, f"{v:.2f}", ha="center", va="center",
                    fontsize=cell_fs, color="white" if v < 0.5 else "black")
    return im


def _draw_valdir_panel(ax: plt.Axes, mat: np.ndarray, group: list[int], title: str, add_colorbar: bool = True):
    n = len(group)
    im = ax.imshow(mat, cmap="RdBu_r", vmin=-1, vmax=1, aspect="auto")
    if add_colorbar:
        plt.colorbar(im, ax=ax, fraction=0.046, pad=0.04, label="cosine sim")
    tick_fs = max(5, 8 - n // 6)
    labels = [str(l) for l in group]
    ax.set_xticks(range(n)); ax.set_xticklabels(labels, fontsize=tick_fs)
    ax.set_yticks(range(n)); ax.set_yticklabels(labels, fontsize=tick_fs)
    ax.set_xlabel("layer"); ax.set_ylabel("layer")
    ax.set_title(title)
    cell_fs = max(4, 7 - n // 6)
    for i in range(n):
        for j in range(n):
            ax.text(j, i, f"{mat[i,j]:.2f}", ha="center", va="center", fontsize=cell_fs)
    return im


# Figures

def fig_trajectory(
    layers_a: list[int], pca_a: dict[int, dict],
    layers_b: list[int], pca_b: dict[int, dict],
    label_a: str, label_b: str,
    model_name: str,
    metric: str,
    ylabel: str,
    title: str,
    out_path: Path,
) -> None:
    fig, ax = plt.subplots(figsize=(8, 4))
    ax.plot(layers_a, [pca_a[l][metric] for l in layers_a],
            "o-", color=COLOR_A, lw=2, ms=6, label=label_a)
    ax.plot(layers_b, [pca_b[l][metric] for l in layers_b],
            "s--", color=COLOR_B, lw=2, ms=6, label=label_b)
    ax.axhline(0, color="grey", lw=0.5, ls=":")
    ax.set_xlabel("Layer")
    ax.set_ylabel(ylabel)
    ax.set_title(f"{model_name} — {title}")
    ax.legend()
    fig.tight_layout()
    fig.savefig(out_path)
    plt.close(fig)
    print(f"  saved {out_path.name}")


def fig_cka_cross_condition(
    contrast_a: dict[int, np.ndarray],
    contrast_b: dict[int, np.ndarray],
    shared_layers: list[int],
    label_a: str, label_b: str,
    model_name: str,
    out_path: Path,
) -> None:
    """
    For each shared layer, compute CKA between the two story-condition geometries.
    Plots as a bar chart so the layer-by-layer stability is easy to read.
    """
    cka_scores = [linear_cka(contrast_a[l], contrast_b[l]) for l in shared_layers]

    fig, ax = plt.subplots(figsize=(max(5, len(shared_layers) * 0.7 + 1), 4))
    bars = ax.bar([str(l) for l in shared_layers], cka_scores,
                  color="#6C8EBF", edgecolor="k", linewidth=0.5)
    ax.set_ylim(0, 1)
    ax.axhline(1.0, color="grey", lw=0.5, ls="--")
    ax.set_xlabel("Layer")
    ax.set_ylabel("Linear CKA")
    ax.set_title(f"{model_name}\nGeometry similarity across story conditions\n"
                 f"({label_a}  vs  {label_b})")
    for bar, val in zip(bars, cka_scores):
        ax.text(bar.get_x() + bar.get_width() / 2, val + 0.01,
                f"{val:.2f}", ha="center", va="bottom", fontsize=7)
    fig.tight_layout()
    fig.savefig(out_path)
    plt.close(fig)
    print(f"  saved {out_path.name}")


def fig_pca_comparison(
    pca_a: dict, pca_b: dict,
    emotions_a: list[str], emotions_b: list[str],
    label_a: str, label_b: str,
    model_name: str, layer: int,
    out_path: Path,
) -> None:
    """Side-by-side PCA scatter for the same layer under each story condition."""
    fig, axes = plt.subplots(1, 2, figsize=(11, 4.5), constrained_layout=True)

    sc = None
    for ax, pca, emotions, label, color in [
        (axes[0], pca_a, emotions_a, label_a, COLOR_A),
        (axes[1], pca_b, emotions_b, label_b, COLOR_B),
    ]:
        pc1, pc2     = pca["pc1"], pca["pc2"]
        valences     = pca["valences"]
        ev           = pca["ev"]
        r_val, r_aro = pca["r_val"], pca["r_aro"]

        sc = ax.scatter(pc1, pc2, c=valences, cmap=VALENCE_CMAP, vmin=-1, vmax=1,
                        s=45, edgecolors="k", linewidths=0.3, zorder=3)

        anchors = {"ecstatic", "depressed", "furious", "serene", "calm",
                   "joyful", "gloomy", "excited", "afraid", "happy", "angry"}
        for i, e in enumerate(emotions):
            if e in anchors:
                ax.annotate(e, (pc1[i], pc2[i]), fontsize=6,
                            xytext=(3, 3), textcoords="offset points")

        ax.axhline(0, color="grey", lw=0.5, ls="--")
        ax.axvline(0, color="grey", lw=0.5, ls="--")
        ax.set_xlabel(f"PC1 ({ev[0]*100:.1f}% var)  r_val={r_val:.3f}")
        ax.set_ylabel(f"PC2 ({ev[1]*100:.1f}% var)  r_aro={r_aro:.3f}")
        ax.set_title(f"{model_name} L{layer}\n{label}")

    fig.colorbar(sc, ax=axes, fraction=0.05, label="valence")
    fig.savefig(out_path)
    plt.close(fig)
    print(f"  saved {out_path.name}")


def fig_cka_panels(
    contrast: dict[int, np.ndarray],
    layers: list[int],
    model_name: str,
    condition_label: str,
    out_dir: Path,
    prefix: str,
) -> np.ndarray:
    """
    Split layers into 3 groups. For each group save a separate CKA heatmap PDF,
    then save one combined 1×3 figure. Returns the full all-pairs CKA matrix.
    """
    groups = _split_layers(layers, 3)
    group_data = []
    for group in groups:
        ng = len(group)
        mat = np.zeros((ng, ng))
        for i, li in enumerate(group):
            for j, lj in enumerate(group):
                mat[i, j] = linear_cka(contrast[li], contrast[lj])
        group_data.append((group, mat))

        size = max(3.5, 0.55 * ng + 1.5)
        fig, ax = plt.subplots(figsize=(size + 0.8, size * 0.85))
        _draw_cka_panel(ax, mat, group,
                        f"{model_name}\n{condition_label}\nLayers {group[0]}–{group[-1]}")
        fig.tight_layout()
        p = out_dir / f"{prefix}_cka_l{group[0]}-{group[-1]}.pdf"
        fig.savefig(p); plt.close(fig)
        print(f"  saved {p.name}")

    ncols = len(group_data)
    fig, axes = plt.subplots(1, ncols, figsize=(5.5 * ncols, 4.5), constrained_layout=True)
    if ncols == 1:
        axes = [axes]
    im = None
    for ax, (group, mat) in zip(axes, group_data):
        im = _draw_cka_panel(ax, mat, group, f"Layers {group[0]}–{group[-1]}", add_colorbar=False)
    fig.colorbar(im, ax=axes, label="linear CKA", fraction=0.02, pad=0.04)
    fig.suptitle(f"{model_name} — Representational similarity (CKA)\n{condition_label}")
    p = out_dir / f"{prefix}_cka_panels.pdf"
    fig.savefig(p); plt.close(fig)
    print(f"  saved {p.name}")

    n = len(layers)
    full_mat = np.zeros((n, n))
    for i, li in enumerate(layers):
        for j, lj in enumerate(layers):
            full_mat[i, j] = linear_cka(contrast[li], contrast[lj])
    return full_mat


def fig_valdir_panels(
    contrast: dict[int, np.ndarray],
    emotions: list[str],
    circumplex: dict,
    layers: list[int],
    model_name: str,
    condition_label: str,
    out_dir: Path,
    prefix: str,
) -> np.ndarray:
    """
    Split layers into 3 groups. For each group save a separate valence-direction
    alignment heatmap PDF, then save one combined 1×3 figure.
    Returns the full all-pairs cosine-similarity matrix.
    """
    val_dirs = {l: _valence_direction(contrast[l], emotions, circumplex) for l in layers}
    groups = _split_layers(layers, 3)
    group_data = []
    for group in groups:
        ng = len(group)
        mat = np.zeros((ng, ng))
        for i, li in enumerate(group):
            for j, lj in enumerate(group):
                mat[i, j] = float(np.dot(val_dirs[li], val_dirs[lj]))
        group_data.append((group, mat))

        size = max(3.5, 0.55 * ng + 1.5)
        fig, ax = plt.subplots(figsize=(size + 0.8, size * 0.85))
        _draw_valdir_panel(ax, mat, group,
                           f"{model_name}\n{condition_label}\nLayers {group[0]}–{group[-1]}")
        fig.tight_layout()
        p = out_dir / f"{prefix}_valdir_l{group[0]}-{group[-1]}.pdf"
        fig.savefig(p); plt.close(fig)
        print(f"  saved {p.name}")

    ncols = len(group_data)
    fig, axes = plt.subplots(1, ncols, figsize=(5.5 * ncols, 4.5), constrained_layout=True)
    if ncols == 1:
        axes = [axes]
    im = None
    for ax, (group, mat) in zip(axes, group_data):
        im = _draw_valdir_panel(ax, mat, group, f"Layers {group[0]}–{group[-1]}", add_colorbar=False)
    fig.colorbar(im, ax=axes, label="cosine sim", fraction=0.02, pad=0.04)
    fig.suptitle(f"{model_name} — Valence direction alignment\n{condition_label}")
    p = out_dir / f"{prefix}_valdir_panels.pdf"
    fig.savefig(p); plt.close(fig)
    print(f"  saved {p.name}")

    n = len(layers)
    full_mat = np.zeros((n, n))
    for i, li in enumerate(layers):
        for j, lj in enumerate(layers):
            full_mat[i, j] = float(np.dot(val_dirs[li], val_dirs[lj]))
    return full_mat


def fig_valdir_stability(
    contrast: dict[int, np.ndarray],
    emotions: list[str],
    circumplex: dict,
    layers: list[int],
    model_name: str,
    condition_label: str,
    out_path: Path,
) -> list[float]:
    """Adjacent-layer valence direction cosine similarity — single line-plot figure."""
    val_dirs = {l: _valence_direction(contrast[l], emotions, circumplex) for l in layers}
    n = len(layers)
    adj_sim = [float(np.dot(val_dirs[layers[i]], val_dirs[layers[i + 1]])) for i in range(n - 1)]
    mid_layers = [(layers[i] + layers[i + 1]) / 2 for i in range(n - 1)]

    fig, ax = plt.subplots(figsize=(max(6, 0.5 * n + 2), 4))
    ax.plot(mid_layers, adj_sim, "o-", color="#2196F3", lw=2, ms=6)
    ax.axhline(0, color="grey", lw=0.5, ls="--")
    ax.set_xticks(layers)
    ax.set_xlabel("layer (midpoint of pair)")
    ax.set_ylabel("cosine similarity")
    ax.set_title(f"{model_name} — Valence direction: adjacent-layer stability\n{condition_label}")
    ax.set_ylim(-1, 1)
    for x, y in zip(mid_layers, adj_sim):
        ax.annotate(f"{y:.2f}", (x, y), textcoords="offset points",
                    xytext=(0, 6), ha="center", fontsize=7)
    fig.tight_layout()
    fig.savefig(out_path)
    plt.close(fig)
    print(f"  saved {out_path.name}")
    return adj_sim


